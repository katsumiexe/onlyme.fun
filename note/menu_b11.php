<h2 class="h2">名刺の作り方</h2>
<div class="exp_box1">
トップメニューの【Making】より作成できます。
『デザイン』で使う名刺のフレームを選択します。<br>
『写真登録』で使う写真を登録します。<br>
『テキスト』で名前やSNSリンク等を設定します。<br>
設定できましたら<span class="red">【<span class="icon_img"></span>】</span>をタップしてください。<br>
<div class="exp_box2">『テキスト』は<span class="red">[<span class="icon_img"></span>config]</span>で設定することも可能で、設定しておけば毎回入力する必要がなくなります。<br></div>
</div>

<div class="exp_box1">
写真の簡単な調整を行えます。<br>
写真を直接スライドさせることで、位置を調整できます。<br>
<span class="red">【<span class="icon_img"></span>】</span>で、使う写真の大きさ、明度、その他を修正することができます。<br>
<span class="red">【<span class="icon_img"></span>】</span>で、テキストの設定を変更することができます。<br>
設定できましたら<span class="red">【<span class="icon_img"></span>】</span>をタップしてください。<br>
<div class="exp_box2">
文字の位置、大きさ等テンプレートの変更はできません。<br>
赤いラインの内側が印刷されます。<br>
仕様上、若干ずれてしまうこともございます。少し余裕をもって設定下さい。
</div>

</div>

<div class="exp_box1">
名刺画像を長押しすることで、名刺データを保存することができます。<br>
名刺データは画像状態(.jpg)で作成されます。<br>
また、<span class="blue">[<span class="icon_img"></span>]</span>から作った名刺をtwitterで公開することもできます。<br>
<div class="exp_box2">twitterで公開する際の名刺には、SNS関連のIDやQRコードは表示されません。<br></div>
</div>
