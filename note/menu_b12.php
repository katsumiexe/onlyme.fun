<h2 class="h2">プリント準備</h2>
<div class="exp_box1">
<span class="ok2">「プリントサービス」</span>⇒<span class="ok2">「写真プリント」</span>の順に選択してください。<br>
その後、作成したプリントデータを以下の方法のどれかでプリンターに転送します。<br><br>

<span style="font-weight:600;">「PrintSmash(SHARP)」を使う。</span><br>
スマホからコピー機にWi-fiで直接名刺データを転送し、プリントします。<br>
インターネット接続不要で、最も簡単な方法です。<br>
<div style="text-align:center;">
<a href="http://www.sharp.co.jp/multicopy/prsm/sp/" class="pslink"><img src="./img/printsmash.png" class="psimg"></a><br><br>
</div>
<span style="font-weight:600;">ネットワークプリントサービスを使う。</span><br>
あらかじめネットワークにデータをアップロードしておき、プリントします<br>
プリント時、スマホが必要ないので、他の人に印刷をお願いすることもできます。<br>
<div style="text-align:center;">
<a href="https://networkprint.ne.jp/sharp_netprint/ja/top.aspx" class="pslink"><img src="./img/network.png" class="psimg"></a><br><br>
</div>

<span style="font-weight:600;">外部保存メディアを使う</span><br>
USBメモリやSDカードなどに保存したデータを読み込ませます。<br>
作成した名刺データを、整理して保存している方にお勧めです。
</div>

<h2 class="h2">プリント手順</h2>
<div class="exp_box1">
名刺データを転送したら<br>
<span class="ok2">「L版写真プリント」</span>⇒<span class="ok2">「2L」</span><br>
の順に選択し、プリント枚数を設定します。<br>
</div>
