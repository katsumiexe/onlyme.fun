<span style="font-weight:600">退会</span><br>
	退会は「Config」よりいつでも行えます。<br>
	退会しますと、全てサービスをご利用いただけなくなります。<br>
	投稿した名刺、プロフィール写真は削除されます。<br>
	応援、評価は削除されません。<br><br>
<span style="font-weight:600">再開</span><br>
	退会から24時間以上経過後、再開が可能です。<br>
	再開されますと、同様にすべてのサービスをご利用いただけます。<br>
	削除された写真は復活できません。<br>
	経験値、レベル、フォロー、フォロワーは退会前のものを継続します。<br>
